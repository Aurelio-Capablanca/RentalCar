    private void txtPreguntasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPreguntasKeyTyped
      
         //Validamos que se puedan poner espacios
      Character e ;
      int k =(char)evt.getKeyChar();
      e=evt.getKeyChar();
      if(!Character.isLetter(e) && e!=KeyEvent.VK_SPACE && k==168 && k==63)
      {
         evt.consume();
      }        
        //Validamos el limite de caracteres
        if (txtPreguntas.getText().length() == 25) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite de caracteres");
        }
        
    }//GEN-LAST:event_txtPreguntasKeyTyped

