    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtPreguntas.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");
        }
        else {
            FuncionesPreguntas ms = new FuncionesPreguntas();
            ms.setCodigo(Integer.parseInt(txtCodigo.getText()));
            ms.setPregunta(txtPreguntas.getText());
            if (ms.guardarPregunta()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
            }else {
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
            }
            verTabla();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (txtCodigo.getText().trim().isEmpty() || txtPreguntas.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");
        }
        else {
            FuncionesPreguntas ms = new FuncionesPreguntas();
            ms.setCodigo(Integer.parseInt(txtCodigo.getText()));
            ms.setPregunta(txtPreguntas.getText());
        
            if (ms.actualizarPregunta()) {
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }else {
                JOptionPane.showMessageDialog(null, "Error al modificar datos");
            }
            verTabla();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (txtCodigo1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");

        } else {
            FuncionesPreguntas obj = new FuncionesPreguntas();
            obj.setCodigo(Integer.parseInt(txtCodigo1.getText()));
            if (obj.consultarPregunta()) {
                txtPreguntas.setText(obj.getPregunta());
            }else {
                JOptionPane.showMessageDialog(null, "Datos no encontrados");
            }
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

