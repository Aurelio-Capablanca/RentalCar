    public void verTabla() {

        String titulos[] = {"ID", "Pregunta"};
        DefaultTableModel tm = new DefaultTableModel(null, titulos);
        try {
            orden = con2.createStatement();
            rs = orden.executeQuery("SELECT * FROM Pregunta");
            
            while (rs.next()) {
                Object Filas[] = {rs.getString(1), rs.getString(2)};
                tm.addRow(Filas);
            }
            tablePreguntas.setModel(tm);
        }
        catch (Exception ex) {
            Logger.getLogger(PanelPreguntas.class.getName()).log(Level.SEVERE, null, ex);
        
        }
    }

