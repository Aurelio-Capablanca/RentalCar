    public void mostrarLic() {
        if (txtBuscar.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Busqueda esta vacio, por favor rellenelo", "Rellene el campo buscar", JOptionPane.WARNING_MESSAGE);

        } else {
            obj.setId(Integer.parseInt(txtBuscar.getText()));
            if (mensa.mostrarLicencia(obj)) {
                txtLicencia.setText(obj.getLicencia());
                prTabla();
                acoplarTabla();

            } else {
                JOptionPane.showMessageDialog(this, "Error, datos no econtrados correctamente, verifique nuevamente", "Datos no encontrados", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

