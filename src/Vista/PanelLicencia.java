    public void prTabla() {
        String titulos[] = {"Codigo", "Licencia"};
        DefaultTableModel tm = new DefaultTableModel(null, titulos);
        try {
            orden = con2.createStatement();
            rs = orden.executeQuery("SELECT * FROM Licencia");

            while (rs.next()) {
                Object Filas[] = {rs.getString(1), rs.getString(2)};
                tm.addRow(Filas);
            }

            TablaLicencia.setModel(tm);

        } catch (SQLException ex) {
            Logger.getLogger(PanelLicencia.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

