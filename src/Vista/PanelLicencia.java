    public void agregarLic() {
        if (txtLicencia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Existen campos vacíos. Por favor, rellenelos", "Campos Vacíos", JOptionPane.WARNING_MESSAGE);

        } else {
            int ids = mensa.idIncremental();

            obj.setId(ids);
            obj.setLicencia(txtLicencia.getText());

            if (mensa.agregarLicencia(obj)) {
                JOptionPane.showMessageDialog(this, "Se ingresaron los datos exitosamente", "Datos Ingresados", JOptionPane.INFORMATION_MESSAGE);
                prTabla();
                limpiar();
                acoplarTabla();

            } else {
                JOptionPane.showMessageDialog(this, "Error, datos no ingresados correctamente, verifique nuevamente", "Datos no ingresados", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

