    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        FuncionesEstadoUnidad obj = new FuncionesEstadoUnidad();
        int ids = obj.idAumento();
        
        obj.setId(ids);
        obj.setEstado(txtEstado.getText());
        
        if (obj.ingresarEstado()) {
            JOptionPane.showMessageDialog(this, "Datos Agregados");
            llenar();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al guardar datos");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        FuncionesEstadoUnidad obj = new FuncionesEstadoUnidad();
        obj.setId(Integer.parseInt(txtBusqueda.getText()));
        
        if(obj.consultarEstado()){
            txtId.setText(String.valueOf(obj.getId()));
            txtEstado.setText(obj.getEstado());
            llenar();
            
        }    
        else{
            JOptionPane.showMessageDialog(this, "Error al consultar datos");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        FuncionesEstadoUnidad obj = new FuncionesEstadoUnidad();
        
        obj.setId(Integer.parseInt(txtId.getText()));
        obj.setEstado(txtEstado.getText());

        if (obj.actualizarEstado()) {
            JOptionPane.showMessageDialog(this, "Datos Actualizados");
            llenar();
            limpiar();

        }else{
            JOptionPane.showMessageDialog(this, "Error al actualizar datos");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

