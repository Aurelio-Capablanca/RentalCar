    String registro = fechaHora.format(date);

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        FuncionesRecurso obj = new FuncionesRecurso();
        int ids = obj.idAumento();

        if(txtNombre.getText().trim().isEmpty() || txtCantida.getText().trim().isEmpty())
        {
           JOptionPane.showMessageDialog(this, "Debe ingresar todo lo solicitado");
        }
        else
        {
            obj.setId(ids);
        obj.setNombre(txtNombre.getText());
        obj.setCantidad(Integer.parseInt(txtCantida.getText()));
        obj.setFecha(registro);

        if (obj.ingresarRecurso()) {
            JOptionPane.showMessageDialog(this, "Datos Agregados");
            llenar();
            acoplarTabla();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar datos");
        }
        }
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        FuncionesRecurso obj = new FuncionesRecurso();

        if (txtBusqueda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Busqueda esta vacio, por favor rellenelo", "Mensaje", JOptionPane.WARNING_MESSAGE);

        } else {
            obj.setId(Integer.parseInt(txtBusqueda.getText()));

            if (obj.consultarEstado()) {
                txtNombre.setText(obj.getNombre());
                txtCantida.setText(String.valueOf(obj.getCantidad()));
                llenar();
                acoplarTabla();

            } else {
                JOptionPane.showMessageDialog(this, "Error al consultar datos");
            }
        }


    }//GEN-LAST:event_btnMostrarActionPerformed

