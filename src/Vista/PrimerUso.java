    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed

        try {

            if (JOptionPane.showConfirmDialog(rootPane, "¿Desea cancelar el registro de su usuario y volver al inicio de sesión, los datos se perderán?",
                    "Hermes Software", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                LoginHermes obj = new LoginHermes();
                obj.setVisible(true);
                dispose();

            } else {
                setDefaultCloseOperation(0);
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Error algo ha salido mal" + e);
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

