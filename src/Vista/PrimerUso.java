    public void Registrar() {

        if (txtRespuesta1.getText().trim().equals("") || txtRespuesta2.getText().trim().equals("")
                || txtCodigo.getText().trim().equals("") || txtConfirmarContraseña.getText().trim().equals("")) {

            JOptionPane.showMessageDialog(this, "Existen campos vacíos. Por favor, rellenelos", "Campos Vacíos", JOptionPane.WARNING_MESSAGE);

        } else if (txtContraseña.getText() == null ? txtConfirmarContraseña.getText() != null : !txtContraseña.getText().equals(txtConfirmarContraseña.getText())) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden, por favor verifique los datos ingresados", "Credenciales Incorrectas", JOptionPane.WARNING_MESSAGE);

        } else if ("Usuario123".equals(txtContraseña.getText())) {
            JOptionPane.showMessageDialog(this, "No puedes ingresar una contraseña por defecto como tu nueva contraseña", "Credenciales Incorrectas", JOptionPane.WARNING_MESSAGE);

        } else if (cmbPregunta1.getSelectedIndex() == 0 && cmbPregunta2.getSelectedIndex() == 0 || cmbPregunta1.getSelectedIndex() == 1 && cmbPregunta2.getSelectedIndex() == 1 || cmbPregunta1.getSelectedIndex() == 2 && cmbPregunta2.getSelectedIndex() == 2 || cmbPregunta1.getSelectedIndex() == 3 && cmbPregunta2.getSelectedIndex() == 3 || cmbPregunta1.getSelectedIndex() == 4 && cmbPregunta2.getSelectedIndex() == 4 || cmbPregunta1.getSelectedIndex() == 5 && cmbPregunta2.getSelectedIndex() == 5) {
            JOptionPane.showMessageDialog(this, "Una pregunta esta siendo seleccionada varias veces. Verifique que las preguntas sean diferentes", "Preguntas Repetidas", JOptionPane.WARNING_MESSAGE);

        } else if (txtRespuesta1.getText() == txtRespuesta2.getText()) {
            JOptionPane.showMessageDialog(this, "No puedes responder lo mismo a diferentes preguntas", "Respuestas Iguales", JOptionPane.WARNING_MESSAGE);

        } else {

            if (txtCodigo.getText().equals("")) {

                JOptionPane.showMessageDialog(this, "El codigo de confirmación esta vacío", "Datos Incompletos", JOptionPane.WARNING_MESSAGE);

            } else if (!txtCodigo.getText().equals("")) {
                obj2.setConfirmacion(txtCodigo.getText());
                if (mensa2.obtenerCodigo(obj2)) {

                    JOptionPane.showMessageDialog(this, "Su codigo es correcto", "Proceso Exitoso", JOptionPane.INFORMATION_MESSAGE);
                    obj.setIdEstado(Integer.parseInt(txtIDEstado.getText()));
                    obj.setConfirmacion(txtCodigo.getText());
                    obj.setRespuesta(txtRespuesta1.getText());
                    obj.setRespuesta2(txtRespuesta2.getText());

                    int idPregunta1 = mensa.obtenerIdPregunta1(cmbPregunta1.getItemAt((cmbPregunta1.getSelectedIndex())));
                    int idPregunta2 = mensa.obtenerIdPregunta2(cmbPregunta1.getItemAt((cmbPregunta2.getSelectedIndex())));
                    obj.setIdPregunta(idPregunta1);
                    obj.setIdPregunta2(idPregunta2);

                    String encriptar = "";

                    try {
                        encriptar = obj3.Encriptar(txtContraseña.getText());
                    } catch (NoSuchAlgorithmException e) {
                        Logger.getLogger(CifrarClave.class.getName()).log(Level.SEVERE, null, e);
                    }
                    obj.setClave(encriptar);

                    if (mensa.actualizarPersona(obj)) {
                        LoginHermes log = new LoginHermes();
                        log.setVisible(true);
                        dispose();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "El codigo de confirmación no coincide", "Datos Erroneos", JOptionPane.WARNING_MESSAGE);
                }

            }

        }
    }

