    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        FuncionesDetalleRecurso obj = new FuncionesDetalleRecurso();
        int ids = obj.idAumento();
        
        obj.setId(ids);
        int Recurso = obj.getIdRecurso(String.valueOf(cmbRecurso.getItemAt(cmbRecurso.getSelectedIndex())));         
        int Unidad = obj.getIdUnidad(String.valueOf(cmbUnidad.getItemAt(cmbUnidad.getSelectedIndex()))); 
        obj.setIdRecurso(Recurso);
        obj.setIdUnidad(Unidad);
        
        if (obj.ingresarDetalle()) {
            JOptionPane.showMessageDialog(this, "Datos Agregados");
            llenar();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al guardar datos");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        FuncionesDetalleRecurso obj = new FuncionesDetalleRecurso();
        
        obj.setId(Integer.parseInt(txtId.getText()));
        int Recurso = obj.getIdRecurso(String.valueOf(cmbRecurso.getItemAt(cmbRecurso.getSelectedIndex())));         
        int Unidad = obj.getIdUnidad(String.valueOf(cmbUnidad.getItemAt(cmbUnidad.getSelectedIndex())));         
        obj.setIdRecurso(Recurso);
        obj.setIdUnidad(Unidad);
        
        if (obj.actualizarDetalle()) {
            JOptionPane.showMessageDialog(this, "Datos Actualizados");
            llenar();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al actualizar datos");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        FuncionesDetalleRecurso obj = new FuncionesDetalleRecurso();
        obj.setId(Integer.parseInt(txtBusqueda.getText()));

        if (obj.consultarDetalle()) {
            cmbRecurso.setSelectedItem(obj.itemRecurso());
            cmbUnidad.setSelectedItem(obj.itemUnidad());
            llenar();
            

        }else{
            JOptionPane.showMessageDialog(this, "Error al consultar datos");
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

