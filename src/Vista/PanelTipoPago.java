    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        
        if(txtTipoPago.getText().trim().equals(""))
        {
          JOptionPane.showMessageDialog(this, "Existen campos vac√≠os. Por favor, rellenelos", "Mensaje", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        
        ConstructorTipoPago xd = new ConstructorTipoPago();
        FuncionesTipoPago obj = new FuncionesTipoPago();
        int ids = obj.idIncremental();
        xd.setIdPago(ids);
        xd.setTipoPago(txtTipoPago.getText());
        if (obj.guardarTipoPago(xd)) {
          JOptionPane.showMessageDialog(this, "Datos Agregados");
            llenar();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al guardar datos");
        }
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        if(txtCodigo.getText().trim().equals("") || txtTipoPago.getText().trim().equals("") )
        {
            JOptionPane.showMessageDialog(this, "Existen campos vac√≠os. Por favor, rellenelos", "Mensaje", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
           ConstructorTipoPago xd = new ConstructorTipoPago();
        FuncionesTipoPago obj = new FuncionesTipoPago();
        
        xd.setIdPago(Integer.parseInt(txtCodigo.getText()));
        xd.setTipoPago(txtTipoPago.getText());
        if (obj.actualizarPago(xd)) {
          JOptionPane.showMessageDialog(this, "Datos Actualizados");
            llenar();
            limpiar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al actualizar datos");
        }
        }
        
       
        
    }//GEN-LAST:event_btnActualizarActionPerformed

