    private void listTipoPagoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listTipoPagoMouseClicked
        // TODO add your handling code here:
        int fila = listTipoPago.getSelectedRow();
        txtCodigo.setText(String.valueOf(listTipoPago.getValueAt(fila, 0)));
        txtTipoPago.setText(String.valueOf(listTipoPago.getValueAt(fila, 1)));
        btnAgregar.setEnabled(false);
        btnActualizar.setEnabled(true);
    }//GEN-LAST:event_listTipoPagoMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        
        if(txtBusqueda.getText().trim().equals("")  )
        {
            JOptionPane.showMessageDialog(this, "Existen campos vac√≠os. Por favor, rellenelos", "Mensaje", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
        ConstructorTipoPago xd = new ConstructorTipoPago();
        FuncionesTipoPago obj = new FuncionesTipoPago();
        
        xd.setIdPago(Integer.parseInt(txtBusqueda.getText()));
        if (obj.mostrarPago(xd)) {
            txtCodigo.setText(Integer.toString(xd.getIdPago()));
            txtTipoPago.setText(xd.getTipoPago());
            llenar();
        }else{
            JOptionPane.showMessageDialog(this, "Error al consultar datos");
        }
        btnAgregar.setEnabled(false);
        btnActualizar.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_btnBuscarMouseClicked

    private void txtTipoPagoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTipoPagoKeyTyped
        char car = evt.getKeyChar();
        if ((car < 'a' || car > 'z') && (car < 'A' || car > 'Z')  && (car != (char) KeyEvent.VK_ENTER) && (car != (char) KeyEvent.VK_BACK_SPACE)) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
    }//GEN-LAST:event_txtTipoPagoKeyTyped

