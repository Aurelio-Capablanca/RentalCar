    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtTipoViaje.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");
        }
        else {
            FuncionesTipoViaje ms = new FuncionesTipoViaje();
            ms.setCodigo(Integer.parseInt(txtCodigo.getText()));
            ms.setNombre(txtTipoViaje.getText());
            if (ms.guardarTipoViaje()) {
                JOptionPane.showMessageDialog(null, "Datos Guardados");
            }else {
                JOptionPane.showMessageDialog(null, "Error al guardar datos");
            }
            verTabla();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (txtCodigo.getText().trim().isEmpty() || txtTipoViaje.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");
        }
        else {
            FuncionesTipoViaje ms = new FuncionesTipoViaje();
            ms.setCodigo(Integer.parseInt(txtCodigo.getText()));
            ms.setNombre(txtTipoViaje.getText());
        
            if (ms.actualizarTipoViaje()) {
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }else {
                JOptionPane.showMessageDialog(null, "Error al modificar datos");
            }
            verTabla();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (txtBusqueda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar todo lo solicitado");

        } else {
            FuncionesTipoViaje obj = new FuncionesTipoViaje();
            obj.setCodigo(Integer.parseInt(txtCodigo.getText()));
            if (obj.consultarTipoViaje()) {
                txtTipoViaje.setText(obj.getNombre());
            }else {
                JOptionPane.showMessageDialog(null, "Datos no encontrados");
            }
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

