    public void verTabla() {

        String titulos[] = {"ID", "Tipo de Viaje"};
        DefaultTableModel tm = new DefaultTableModel(null, titulos);
        try {
            orden = con2.createStatement();
            rs = orden.executeQuery("SELECT * FROM TipoViaje");
            
            while (rs.next()) {
                Object Filas[] = {rs.getString(1), rs.getString(2)};
                tm.addRow(Filas);
            }
            tableTipoViaje.setModel(tm);
        }
        catch (Exception ex) {
            Logger.getLogger(PanelTipoViaje.class.getName()).log(Level.SEVERE, null, ex);
        
        }
    }

