    private void btnExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExaminarActionPerformed
        JFileChooser ex = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG", "jpg", "png");
        ex.setFileFilter(fil);

        int ap = ex.showOpenDialog(this);
        if (ap == JFileChooser.APPROVE_OPTION) {
            String ruta = ex.getSelectedFile().getAbsolutePath();
            String filu = ex.getSelectedFile().toString();
            lblLogoEmpresa.setIcon(new ImageIcon(filu));
            ImageIcon icon = new ImageIcon(filu);
            Image gdx = icon.getImage();
            Image newgdx = gdx.getScaledInstance(250, 250, java.awt.Image.SCALE_SMOOTH);
            ImageIcon ui = new ImageIcon(newgdx);
            lblLogoEmpresa.setIcon(ui);

            try {
                String resize = DatatypeConverter.printBase64Binary(Files.readAllBytes(Paths.get(filu)));
                txtImagen.setText(String.valueOf(resize));
            } catch (IOException e) {
                System.err.println(e.toString());
            }

        }

    }//GEN-LAST:event_btnExaminarActionPerformed

    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed

        if (txtNombre.getText().trim().equals("") || txtNIT.getText().trim().equals("")
                || txtRepresentante.getText().trim().equals("") || txtTelefono.getText().trim().equals("")
                || txtUbicacion.getText().trim().equals("")) {

            JOptionPane.showMessageDialog(this, "Existen campos vacÃ­os. Por favor rellenelos", "Campos VacÃ­os", JOptionPane.WARNING_MESSAGE);

        } else {

            int ids = mensa.idIncremental();

            obj.setIdEmpresa(ids);
            obj.setNombre(txtNombre.getText());
            obj.setRepresentante(txtRepresentante.getText());
            obj.setTelefono(txtTelefono.getText());
            obj.setUbicacion(txtUbicacion.getText());
            obj.setNit(txtNIT.getText());
            obj.setRegistro(convertido);
            obj.setLogo(txtImagen.getText());

            if (mensa.ingresarEmpresa(obj)) {

                JOptionPane.showMessageDialog(this, "La empresa fue agregada exitosamente", "Empresa Registrada", JOptionPane.INFORMATION_MESSAGE);
                PrimerRoot pr = new PrimerRoot();
                pr.setVisible(true);
                this.dispose();
            }

        }

    }//GEN-LAST:event_btnContinuarActionPerformed

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
         Character e;
        e = evt.getKeyChar();
        if (!Character.isLetter(e) && e != KeyEvent.VK_SPACE) {
            evt.consume();
        }

        //Validamos el limite de caracteres
        if (txtNombre.getText().length() == 25) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite de caracteres");
        }
    }//GEN-LAST:event_txtNombreKeyTyped

