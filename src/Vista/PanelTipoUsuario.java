    public void prTabla() {
        String titulos[] = {"Codigo", "TipoPersonal"};
        DefaultTableModel tm = new DefaultTableModel(null, titulos);
        try {
            orden = con2.createStatement();
            rs = orden.executeQuery("SELECT * FROM TipoPersona");

            while (rs.next()) {
                Object Filas[] = {rs.getString(1), rs.getString(2)};
                tm.addRow(Filas);
            }

            listTipoUsuario.setModel(tm);

        } catch (SQLException ex) {
            Logger.getLogger(PanelTipoUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

