    public void eliminarTipoPerso() {

        obj.setId(Integer.parseInt(txtCodigo.getText()));

        int eliminar = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea eliminar?",
                "Atenccion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (eliminar == 0) {
            if (mensa.bajaTipoPersona(obj)) {
                JOptionPane.showMessageDialog(this, "Se eliminaron los datos exitosamente");
                prTabla();
                limpiar();

            } else {
                JOptionPane.showMessageDialog(this, "Error, datos no eliminados correctamente, Verifique nuevamente");

            }
        }
    }

    public void MostrarTipoPerso() {
        if (txtBusqueda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo Busqueda esta vacio, por favor rellenelo", "Mensaje", JOptionPane.WARNING_MESSAGE);

        } else {
            obj.setId(Integer.parseInt(txtBusqueda.getText()));
            if (mensa.mostrarTipoPersona(obj)) {
                txtTipoUsuario.setText(obj.getTipo());
                prTabla();
                acoplarTabla();

            } else {
                JOptionPane.showMessageDialog(this, "Error, datos no econtrados correctamente, verifique nuevamente", "Datos no encontrados", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

